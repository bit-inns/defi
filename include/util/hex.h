constexpr uint8_t hex_char_to_uint8t(char c) {
  if (c >= '0' && c <= '9') return c - '0';
  if (c >= 'a' && c <= 'f') return c - 'a' + 0xa;
  if (c >= 'A' && c <= 'F') return c - 'A' + 0xa;
  throw "invalid arg: util::hex - hex_char_to_uint8t func call";
}

constexpr char uint8t_to_hex_char(uint8_t n) {
  if (n <= 9) return '0' + n;
  if (n <= 15) return 'a' + (n - 10);
  throw "invalid arg: util::hex - uint8t_to_hex_char func call";
}

